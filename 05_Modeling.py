from pyspark.sql.functions import col

# Load feature-engineered data
features_df = spark.read.table("cmapss_features")

print("✅ Feature data loaded")
print(f"Shape: {features_df.count()} rows, {len(features_df.columns)} columns")
display(features_df.select("engine_id", "cycle", "RUL", "features_scaled").limit(5))

"""## 🔷 PART A: DIMENSIONALITY REDUCTION (PCA)

### Principal Component Analysis (PCA)

* Reduces high-dimensional sensor data to fewer components
* Captures maximum variance in fewer dimensions
* Helps visualize data and reduces computational complexity
* We'll reduce features to 5 principal components
"""

from sklearn.decomposition import PCA as SklearnPCA
import pandas as pd
import numpy as np

# Convert to pandas for PCA transformation
print("🔄 Converting to pandas for PCA...")
features_pandas = features_df.toPandas()

# Extract features from array column
features_array = np.array(features_pandas['features_scaled'].tolist())

# Fit and transform PCA
n_components = 5
pca_model = SklearnPCA(n_components=n_components)
pca_features = pca_model.fit_transform(features_array)

print("✅ PCA transformation complete")
print(f"Reduced dimensions: {features_array.shape[1]} → {n_components} principal components")

# Display explained variance
explained_variance = pca_model.explained_variance_ratio_
print(f"\n📊 Explained Variance by Component:")
for i, var in enumerate(explained_variance):
    print(f"  PC{i+1}: {var:.4f} ({var*100:.2f}%)")

total_variance = sum(explained_variance)
print(f"\n✅ Total variance explained: {total_variance*100:.2f}%")

# Add PCA features back to pandas DataFrame
features_pandas['pca_features'] = list(pca_features)

# Convert back to Spark DataFrame
pca_df = spark.createDataFrame(features_pandas)

"""## Visualize PCA Components"""

from pyspark.sql.functions import udf, lit
from pyspark.sql.types import FloatType

# Extract individual PC components
@udf(FloatType())
def extract_pc(v, idx):
    return float(v[idx])

pca_viz = pca_df.withColumn("PC1", extract_pc(col("pca_features"), lit(0))) \
                 .withColumn("PC2", extract_pc(col("pca_features"), lit(1))) \
                 .withColumn("PC3", extract_pc(col("pca_features"), lit(2)))

print("📈 PCA Scatter Plot: PC1 vs PC2 colored by RUL")
display(pca_viz.select("PC1", "PC2", "RUL"))

"""## 🔷 PART B: CLUSTERING (K-MEANS)

### K-Means Clustering

* Groups engines into health clusters
* Uses PCA features for clustering
* k=3 clusters: Healthy, Degrading, Critical
"""

from sklearn.cluster import KMeans as SklearnKMeans
import pandas as pd
import numpy as np

# Convert to pandas for K-Means clustering
print("🔄 Converting to pandas for K-Means...")
pca_viz_pandas = pca_viz.toPandas()

# Extract PCA features from array column
pca_features_array = np.array(pca_viz_pandas['pca_features'].tolist())

# Configure and fit K-Means
kmeans_model = SklearnKMeans(n_clusters=3, random_state=42, n_init=10)
cluster_labels = kmeans_model.fit_predict(pca_features_array)

# Add cluster labels to pandas DataFrame
pca_viz_pandas['cluster'] = cluster_labels

# Convert back to Spark DataFrame
cluster_df = spark.createDataFrame(pca_viz_pandas)

print("✅ K-Means clustering complete")

# Cluster centers
centers = kmeans_model.cluster_centers_
print(f"\n📊 Cluster Centers:")
for i, center in enumerate(centers):
    print(f"  Cluster {i}: {center}")

# Cluster distribution
print("\n📊 Cluster Distribution:")
cluster_df.groupBy("cluster").count().orderBy("cluster").show()

"""## Analyze Clusters"""

from pyspark.sql.functions import mean, min, max, count

# Analyze RUL distribution per cluster
print("📊 Average RUL per Cluster:")
cluster_df.groupBy("cluster").agg(
    mean("RUL").alias("avg_RUL"),
    min("RUL").alias("min_RUL"),
    max("RUL").alias("max_RUL"),
    count("RUL").alias("count")
).orderBy("cluster").show()

print("\n🔍 Cluster Interpretation:")
print("Cluster with highest avg_RUL → Healthy engines")
print("Cluster with lowest avg_RUL → Critical/Failing engines")
print("Middle cluster → Degrading engines")

"""## Visualize Clusters"""

# Visualize clusters in PCA space
print("📈 Cluster Visualization: PC1 vs PC2 colored by cluster")
display(cluster_df.select("PC1", "PC2", "cluster"))

# Visualize RUL by cluster
print("\n📈 RUL Distribution by Cluster:")
display(cluster_df.select("cluster", "RUL"))

"""## 🔷 PART C: RUL PREDICTION (RANDOM FOREST)

### Random Forest Regressor

* Predicts Remaining Useful Life (RUL)
* Uses scaled features as input
* Ensemble of decision trees for robust predictions
"""

# Split data: 80% train, 20% test
train_df, test_df = cluster_df.randomSplit([0.8, 0.2], seed=42)

print(f"📊 Train set: {train_df.count()} rows")
print(f"📊 Test set: {test_df.count()} rows")

train_df.describe().show()

from sklearn.ensemble import RandomForestRegressor as SklearnRF
import pandas as pd
import numpy as np

# Convert train/test data to pandas
print("🔄 Converting to pandas for Random Forest...")
train_pandas = train_df.toPandas()
test_pandas = test_df.toPandas()

# Extract features and labels
X_train = np.array(train_pandas['features_scaled'].tolist())
y_train = train_pandas['RUL'].values
X_test = np.array(test_pandas['features_scaled'].tolist())
y_test = test_pandas['RUL'].values

# Configure and train Random Forest
print("🔄 Training Random Forest model...")
rf_model = SklearnRF(
    n_estimators=100,
    max_depth=10,
    random_state=42,
    n_jobs=-1
)
rf_model.fit(X_train, y_train)
print("✅ Model training complete!")

# Feature importance
print(f"\n📊 Feature Importances (top 10):")
importances = rf_model.feature_importances_
top_10_idx = np.argsort(importances)[-10:][::-1]
for idx in top_10_idx:
    print(f"  Feature {idx}: {importances[idx]:.4f}")

import pandas as pd
import joblib

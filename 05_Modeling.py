"""## Model Evaluation"""

import pandas as pd
import numpy as np

# Make predictions on test set using scikit-learn model
y_pred = rf_model.predict(X_test)

# Add predictions to test pandas DataFrame
test_pandas['predicted_RUL'] = y_pred

# Convert back to Spark DataFrame
predictions = spark.createDataFrame(test_pandas)

# Display sample predictions
print("📊 Sample Predictions:")
from pyspark.sql.functions import abs as spark_abs
display(predictions.select("engine_id", "cycle", "RUL", "predicted_RUL",
                            spark_abs(col("RUL") - col("predicted_RUL")).alias("error")).limit(20))

from sklearn.metrics import mean_squared_error, mean_absolute_error, r2_score
import numpy as np

# Calculate metrics using scikit-learn (Spark Connect blocks pyspark.ml)
y_true = test_pandas['RUL']
y_pred = test_pandas['predicted_RUL']

# RMSE (Root Mean Squared Error)
rmse = np.sqrt(mean_squared_error(y_true, y_pred))

# MAE (Mean Absolute Error)
mae = mean_absolute_error(y_true, y_pred)

# R² Score
r2 = r2_score(y_true, y_pred)

print("📊 MODEL PERFORMANCE METRICS:")
print(f"  RMSE: {rmse:.2f} cycles")
print(f"  MAE:  {mae:.2f} cycles")
print(f"  R²:   {r2:.4f}")

print("\n✅ Lower RMSE/MAE is better")
print("✅ R² closer to 1.0 is better")

